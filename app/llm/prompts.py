def build_user_prompt(date_str: str, weekday: str, day_element_hanja: str,
                      member_lines: list[str], pair_lines: list[str]) -> str:
    """03 문서의 User Prompt 템플릿을 실제 값으로 채운다."""
    members = "\n".join(member_lines)
    pairs = "\n".join(pair_lines) if pair_lines else "- (없음)"
    return (
        f"오늘은 {date_str} ({weekday}), 오늘의 날짜 오행은 {day_element_hanja} 기운입니다.\n\n"
        f"[그룹 멤버 오행 정보]\n{members}\n\n"
        f"[케미를 생성할 페어 목록]\n{pairs}\n\n"
        "위 정보를 바탕으로 emit_fortunes 도구를 호출해\n"
        "- 멤버별 오늘의 한줄 운세\n"
        "- 주어진 페어별 케미 한줄 코멘트\n"
        "- 그룹 전체 한줄평\n"
        "을 생성하세요."
    )
